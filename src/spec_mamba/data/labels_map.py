if set.intersection(*[set(v) for v in LABELS_MAP.values()]):
    raise ValueError("Label subsets must not intersect")

LABELS = list(LABELS_MAP.keys())
NUM_LABELS = len(LABELS)


def map_label(label: str, index: bool = False) -> str | int:
    """Map labels to label classes."""
    for key, value in LABELS_MAP.items():
        if label in value:
            if index:
                return LABELS.index(key)
            return key

    raise KeyError(f"No mapping found for label '{label}'")
